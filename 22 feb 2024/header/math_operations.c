#include "math_operations.h"

int addition(int num1,int num2){
    return num1+num2;

}


int substract(int num1,int num2){
    return num1-num2;

}


int multiply(int num1,int num2){
    return num1*num2;

}


int divide(int num1,int num2){
    return num1/num2;

}

int remainders(int num1,int num2){
    return num1%num2;

}